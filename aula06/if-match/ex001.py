num = int(input('Digite um número de 1 a 7: '))

match num:
    case  1:
        print('Domingo, final de semana')
    case 2:
        print('Segunda-feira, dia de semana')
    case 3:
        print('Terça-feira, dia de semana')
    case 4:
        print('Quarta-feira, dia de semana')
    case 5:
        print('Quinta-feira, dia de semana')
    case 6:
        print('Sexta-feira, dia de semana')
    case 7:
        print('Sábado, fim de semana')